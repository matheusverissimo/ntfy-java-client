package ntfyclient;

/**
 * Defines the priority level of the notification
 */
public class Priority {

	/**
	 * Maximum priority
	 */
	public final static Integer MAX = 5;
	/**
	 * High priority
	 */
	public final static Integer HIGH = 4;
	/**
	 * Default priority 
	 */
	public final static Integer DEFAULT = 3;
	/**
	 * Low priority
	 */
	public final static Integer LOW = 2;
	/**
	 * Minimum priority
	 */
	public final static Integer MIN = 1;
}
